export interface ProjectFileBundle {
  files: GeneratedFile[]
}

/** 生成 build.gradle / gradle.properties / mods.toml 等项目文件 */
export function generateProjectFiles(
  project: CodegenProject,
  options: { includeReadme?: boolean; includeGitignore?: boolean } = {},
): ProjectFileBundle {
  const files: GeneratedFile[] = []

  files.push({ path: 'build.gradle', content: genBuildGradle() })
  files.push({ path: 'gradle.properties', content: genGradleProperties(project) })
  files.push({ path: 'settings.gradle', content: genSettingsGradle(project) })
  files.push({
    path: 'src/main/resources/META-INF/mods.toml',
    content: genModsToml(project),
  })
  files.push({
    path: 'src/main/resources/pack.mcmeta',
    content: genPackMcmeta(),
  })

  if (options.includeReadme !== false) {
    files.push({ path: 'README.md', content: genReadme(project) })
  }
  if (options.includeGitignore !== false) {
    files.push({ path: '.gitignore', content: genGitignore() })
  }

  return { files }
}

/** 生成 gradle wrapper 脚本（可选，UI 默认包含） */
export function generateGradleWrapper(): GeneratedFile[] {
  return [
    { path: 'gradlew', content: genGradlewUnix(), executable: true },
    { path: 'gradlew.bat', content: genGradlewWindows() },
    {
      path: 'gradle/wrapper/gradle-wrapper.properties',
      content: genGradleWrapperProps(),
    },
  ]
}
