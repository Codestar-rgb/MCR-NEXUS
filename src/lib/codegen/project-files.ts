/**
 * Forge 1.20.1 项目骨架文件生成器（Task 4-D）
 *
 * 生成 Forge MDK 项目所需的非 Java 文件：
 *   - build.gradle           (ForgeGradle 6.0.x 配置)
 *   - gradle.properties      (版本与 mod 元信息)
 *   - settings.gradle
 *   - gradlew / gradlew.bat  (Forge MDK 自带 wrapper 脚本)
 *   - gradle/wrapper/gradle-wrapper.properties
 *   - gradle/wrapper/gradle-wrapper.jar (二进制占位 — 实际下载需用户首次运行 ./gradlew)
 *   - src/main/resources/META-INF/mods.toml
 *   - src/main/resources/pack.mcmeta
 *   - README.md
 *   - .gitignore
 *
 * 设计要点：
 *  - gradle.properties 中所有 ${var} 占位由 build.gradle / mods.toml 在
 *    processResources 阶段替换。
 *  - mods.toml 的 loaderVersion 必须 ≥ [47,) 才能匹配 Forge 47.3.x。
 *  - Java 17 是 1.20.1 的最低要求。
 *  - gradlew 脚本为 Forge MDK 标准 wrapper，用户首次运行 ./gradlew 时
 *    会自动下载 gradle-wrapper.jar（这里 jar 不内置，避免 ZIP 过大）。
 */

import type { CodegenProject, GeneratedFile } from './types'
import { buildJavaPackage } from './naming'

/* ------------------------------------------------------------------ */
/* gradle.properties                                                   */
/* ------------------------------------------------------------------ */

function genGradleProperties(project: CodegenProject): string {
  const mc = project.mcVersion || '1.20.1'
  const forge = project.forgeVersion || '47.3.7'
  const groupId = buildJavaPackage(project.modId, project.author)
  return `# Gradle / JVM 配置
org.gradle.jvmargs=-Xmx3G
org.gradle.daemon=false
org.gradle.debug=false

# Minecraft / Forge 版本（ForgeGradle 6.0.x）
minecraft_version=${mc}
minecraft_version_range=[1.20.1,1.21)
forge_version=${forge}
forge_version_range=[47,)
loader_version_range=[47,)
mapping_channel=official
mapping_version=${mc}

# 模组元信息
mod_id=${project.modId}
mod_name=${project.name}
mod_license=MIT
mod_version=${project.version}
mod_group_id=${groupId}
mod_authors=${project.author}
mod_description=${(project.description || 'Generated by NexCube IDE').replace(/\n/g, ' ')}
`
}

